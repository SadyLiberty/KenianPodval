        static void StartGame()
        {
            Console.WriteLine("Начинаем спуск в кенийский подвал\n");

            for (int room = 1; room <= 15; room++)
            {
                Console.WriteLine($"Комнат {room}/15");
                ProcessRoom(room);

                if (playerHP <= 0)
                {
                    EndGame(false);
                    return;
                }

                Console.WriteLine();
                Console.ReadKey();
            }

            FightBoss();
        }

        static void ProcessRoom(int roomNumber)
        {
            int eventType = rand.Next(1, 9); // 8 типов событий

            switch (eventType)
            {
                case 1: FightMonster(); break;
                case 2: Trap(); break;
                case 3: OpenChest(); break;
                case 4: VisitTORGOVEC(); break;
                case 5: VisitAltar(); break;
                case 6: MeetDarkMag(); break;
                case 7: ZAGADKA(); break;
                default: EmptyRoom(); break;
            }
        }

        static void FightMonster()
        {
            int monsterHP = rand.Next(20, 51);
            int monsterAttack = rand.Next(5, 16);
            Console.WriteLine(" АФРИКАНСКОЕ ЧУДОВИЩЕ!");
            Console.WriteLine($"Чудовище: {monsterHP} HP, атака {monsterAttack}");

            while (monsterHP > 0 && playerHP > 0)
            {
                Console.WriteLine("\n1. Меч  2. Лук  3. отвара");
                string choice = Console.ReadLine();

                if (choice == "1")
                {
                    int damage = rand.Next(swordDamage, swordDamage + 11);
                    monsterHP -= damage;
                    Console.WriteLine($"Вы нанесли {damage} урона мечом!");
                }
                else if (choice == "2" && arrows > 0)
                {
                    int damage = rand.Next(bowDamage, bowDamage + 11);
                    monsterHP -= damage;
                    arrows--;
                    Console.WriteLine($"Вы выстрелили! {damage} урона. Стрел: {arrows}");
                }
                else if (choice == "3" && potions > 0)
                {
                    UsePotion();
                    continue;
                }
                else
                {
                    Console.WriteLine("Грешен избор");
                    continue;
                }

                if (monsterHP > 0)
                {
                    int playerDamage = rand.Next(monsterAttack);
                    playerHP -= playerDamage;
                    Console.WriteLine($"Монстр атакует! Вы теряете {playerDamage} HP.");
                }
            }

            if (monsterHP <= 0)
            {
                Console.WriteLine(" ЧУДОВИЩЕТО  ПОБЕДЕНО! + 5 Простой ящик");
                gold += 5;
            }
        }

        static void Trap()
        {
            int damage = rand.Next(5, 21);
            playerHP -= damage;
            Console.WriteLine($" ЛОВУШКА! Вы теряете {damage} HP.");
        }

        static void OpenChest()
        {
            bool isCursed = rand.Next(0, 2) == 1;
            if (isCursed)
            {
                Console.WriteLine("Простой ящик");
                gold += 8;
                maxHP -= 10;
                playerHP = Math.Min(playerHP, maxHP);
                Console.WriteLine($"+8 кенийских рублей, но максимум HP уменьшен до {maxHP} неприятность ");
            }
            else
            {
                Console.WriteLine(" Простой ящик");
                int reward = rand.Next(1, 4);
                if (reward == 1) { gold += 5; Console.WriteLine("+5 кенийских рублей"); }
                else if (reward == 2) { potions++; Console.WriteLine("+1 отвар"); }
                else { arrows += 3; Console.WriteLine("+3 стрелы"); }
            }
        }

